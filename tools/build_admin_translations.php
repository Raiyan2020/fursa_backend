function exportPhpArray(array $data): string
{
    $export = var_export($data, true);
    $export = preg_replace('/^([ ]*)(.*)/m', '$1$2', $export);
    $export = str_replace(['array (', ')'], ['[', ']'], $export);
    $export = preg_replace('/=>\s*\n\s*\[/', '=> [', $export);

    return "<?php\n\nreturn ".$export.";\n";
}

